function StartGame(gameJson, gameConfigJson)
    tGame = CJson.decode(gameJson)
    tConfig = CJson.decode(gameConfigJson)

    for iX = 1, tGame.Cols do
        tFloor[iX] = {}    
        tBackground[iX] = {}
        for iY = 1, tGame.Rows do
            tFloor[iX][iY] = CHelp.ShallowCopy(tFloorStruct) 
        end
    end

    for _, iId in pairs(tGame.Buttons) do
        tButtons[iId] = CHelp.ShallowCopy(tButtonStruct)
    end

    iPrevTickTime = CTime.unix()
    OffsetTimer()

    CAudio.PlaySync(tGame.audio)
end

function NextTick()
    if iGameState == GAMESTATE_GAME then
        GameTick()
    end

    if iGameState == GAMESTATE_FINISH then
        return tGameResults
    end    

    CTimer.CountTimers((CTime.unix() - iPrevTickTime) * 1000)
    iPrevTickTime = CTime.unix()
end

function GameTick()
    SetGlobalColorBright(CColors.NONE, tConfig.Bright)

    for iY = 1, tGame.Rows do
        for iX = 1, tGame.Cols do
            local tBackground = GetBackgroundColor(iX, iY)
            if tBackground ~= nil then
                tFloor[iX][iY].iColor = tBackground.color
                tFloor[iX][iY].iBright = tBackground.bright
            end

            if tGame.picture[iY] and tGame.picture[iY][iX] and tGame.picture[iY][iX].color ~= CColors.NONE then
                tFloor[iX][iY].iColor = tGame.picture[iY][iX].color
                tFloor[iX][iY].iBright = tGame.picture[iY][iX].bright
            end
        end
    end
end

